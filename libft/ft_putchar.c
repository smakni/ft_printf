#include "libft.h"

void    ft_putchar(wint_t c)
{
    if (c <= 127)
        ft_putchar1(c);
    else if (c <= 2047)
        ft_putchar2(c);
    else if ((c <= 55295 && c >= 57344)
             || (c <= 64975 && c >= 65007) || c <= 65533)
        ft_putchar3(c);
    else if ((c >= 65536 && c <= 131069) || (c >= 131072 && c <= 196605) 
                || (c >= 196607 && c <= 983037) 
                || (c >= 983040 && c <= 1048573)
                || (c >= 1048576 && c <= 1114109))
        ft_putchar4(c);
}
