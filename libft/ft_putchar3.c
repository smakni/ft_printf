#include "libft.h"

void    ft_putchar3(wint_t c)
{
    ft_putchar1((c >> 12) + 0xE0);
    ft_putchar1(((c >> 6) & 0x3F) + 0x80);
    ft_putchar1((c & 0x3F) + 0x80);
}