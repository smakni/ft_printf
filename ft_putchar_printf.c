#include "ft_printf.h"

char	*ft_putchar_printf(wint_t c, t_format *arg, char *str)
{	 
    if (c <= 127)
	{
		str[0] = c;
		str[1] = '\0';
	}
    else if (c <= 2047 && MB_CUR_MAX >= 2)
    {
		str[0] = (c >> 6) + 0xC0;
		str[1] = (c & 0x3F) + 0x80;
		str[2] = '\0';
    }
	else if ((c <= 55295 || (c >= 57344 && c <= 64975) 
			|| (c >= 65008 && c <= 65533)) && MB_CUR_MAX >= 3)
    {
		str[0] = (c >> 12) + 0xE0;
		str[1] = ((c >> 6) & 0x3F) + 0x80;
		str[2] = (c & 0x3F) + 0x80;
		str[3] = '\0';

    }
    else if (((c >= 65536 && c <= 131069) || (c >= 131072 && c <= 196605)
	    	|| (c >= 917504 && c <= 983037)
			|| (c >= 983040 && c <= 1048573)
			|| (c >= 1048576 && c <= 1114109)) && MB_CUR_MAX >= 4)
    {
		str[0] = (c >> 18) + 0xF0;
		str[1] = ((c >> 12) & 0x3F) + 0x80;
		str[2] = ((c >> 6) & 0x3F) + 0x80;
		str[3] = (c & 0x3F) + 0x80;
		str[4] = '\0';
    }
    else
        arg->count = -1;
	return (str);
}
